        {
            IntPtr ptr = new IntPtr();

            CrossThreadHelper.InvokeAction(control, c => ptr = c.Handle);

            return ControlsHelper.PrintWindow(ptr);
        }
        public static Image PrintToImage(this Control control, IntPtr hWnd)
        {
